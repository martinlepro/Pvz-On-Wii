GRRLIB_texImg* BitmapFont_CreateTexture(void)
{
    int charW = 8;
    int charH = 8;
    int cols  = 16;
    int rows  = 16;
    int texW  = cols * charW;
    int texH  = rows * charH;

    GRRLIB_texImg* tex = GRRLIB_CreateEmptyTexture(texW, texH);
    if (!tex) return NULL;

    for (int c = 0; c < 256; c++)
    {
        int cx = (c % cols) * charW;
        int cy = (c / cols) * charH;

        for (int row = 0; row < charH; row++)
        {
            unsigned char bits;
            if (c >= 0x20 && c <= 0x7F)
                bits = kFont8x8[c - 0x20][row];
            else
                bits = 0x00;

            for (int col = 0; col < charW; col++)
            {
                int px = cx + col;
                int py = cy + row;
                u32 color;

                if (bits & (0x80 >> col))
                    color = 0xFFFFFFFF;
                else
                    color = 0x00000000;

                tex->data[py * texW + px] = color;
            }
        }
    }

    return tex;
}
