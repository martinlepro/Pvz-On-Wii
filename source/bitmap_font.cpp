GRRLIB_texImg* BitmapFont_CreateTexture(void)
{
    int charW = 8;
    int charH = 8;
    int cols  = 16;
    int rows  = 16;
    int texW  = cols * charW;
    int texH  = rows * charH;

    GRRLIB_texImg* tex = GRRLIB_CreateEmptyTexture(texW, texH);
    if (!tex) return NULL;

    for (int c = 0; c < 256; c++)
    {
        int cx = (c % cols) * charW;
        int cy = (c / cols) * charH;

        for (int row = 0; row < charH; row++)
        {
            unsigned char bits;
            if (c >= 0x20 && c <= 0x7F)
                bits = kFont8x8[c - 0x20][row];
            else
                bits = 0x00;

            for (int col = 0; col < charW; col++)
            {
                int px = cx + col;
                int py = cy + row;
                u32 color;

                /* kFont8x8 is authored bit 7 = the LEFTMOST pixel of each
                 * row (the standard MSB-first convention every classic 8x8
                 * font, including this one, uses) -- e.g. row 0 of 'A'
                 * (0x3C = 00111100) is meant to read "..####.." for the
                 * apex, and row 2 of '0' (0x76 = 01110110) encodes its
                 * diagonal slash going the correct way. `0x01 << col` reads
                 * bit 0 first instead of bit 7, i.e. every row backwards,
                 * so every glyph came out as its own horizontal mirror
                 * image. `bits & (0x80 >> col)` reads the bits in the order
                 * the table was actually authored in. */
                if (bits & (0x80 >> col))
                    color = 0xFFFFFFFF;
                else
                    color = 0x00000000;

                /* GRRLIB textures use the GX hardware's GX_TF_RGBA8 format,
                 * which packs pixels in 4x4-pixel tiled blocks (an
                 * alpha/red plane followed by a green/blue plane per
                 * block) -- it is NOT a plain row-major array of u32
                 * pixels. The previous code here wrote directly into
                 * tex->data with `((u32*)tex->data)[py*texW+px] = color`,
                 * which scrambles every glyph's pixels into noise, since
                 * the GX hardware reads them back out using the tiled
                 * addressing scheme, not the linear one they were written
                 * with. That's why text still didn't render correctly even
                 * after GRRLIB_InitTileSet was wired up below.
                 * GRRLIB_SetPixelTotexImg() knows the real tiled layout and
                 * writes each pixel to the correct byte offset for it. */
                GRRLIB_SetPixelTotexImg(px, py, tex, color);
            }
        }
    }

    /* The writes above went through the CPU's data cache; the GX hardware
     * reads textures straight from main memory, so without flushing the
     * cache down explicitly here the glyphs could still appear blank (or
     * show stale garbage) until something unrelated happened to evict
     * those cache lines first. */
    GRRLIB_FlushTex(tex);

    /* GRRLIB_Printf() only knows how to draw text from a texture that has
     * been registered as a tileset: it needs the per-glyph cell size and the
     * ASCII code of "tile 0" so it can turn each character into a cell
     * index (GRRLIB does `character - tilestart` internally). Without this
     * call the texture has no tile geometry at all and GRRLIB_Printf
     * silently draws nothing -- this was why no text ever appeared
     * anywhere in the game (menus, HUD, victory/defeat screens...), with or
     * without a real font.ttf present.
     *
     * tilestart is 0 here, NOT the usual 32: the fill loop above places
     * every glyph at cell index == its own ASCII code (0-255, with blank
     * cells for the non-printable ones below 0x20), instead of the more
     * common convention of packing only the printable range starting at
     * cell 0 for the space character. Passing 32 here would look "more
     * standard" but would shift every glyph by 32 cells and print the wrong
     * letters. */
    GRRLIB_InitTileSet(tex, charW, charH, 0);

    return tex;
}
